public class MaxValue {
    public static void main(String[] args) {
        int x = 678;
        int y = 35;
        int maxValue = 0;
        if (x > y)
        {
            maxValue = x;
        } else maxValue = y;
        System.out.println("Maximal Value is " + maxValue);
    }
}
